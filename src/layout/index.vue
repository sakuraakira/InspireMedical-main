<template>
  <div :class="classObj" class="app-wrapper">
    <div v-if="device==='mobile'&&sidebar.opened" class="drawer-bg" @click="handleClickOutside" />
    <sidebar class="sidebar-container" />
    <div :class="{hasTagsView:needTagsView}" class="main-container">
      <div :class="{'fixed-header':fixedHeader}">
        <navbar />
        <tags-view v-if="needTagsView" />
      </div>
      <app-main />
      <right-panel v-if="showSettings">
        <settings />
      </right-panel>
    </div>
  </div>
</template>

<script>
import RightPanel from '@/components/RightPanel'
import { AppMain, Navbar, Settings, Sidebar, TagsView } from './components'
import ResizeMixin from './mixin/ResizeHandler'
import { mapState } from 'vuex'
import { getAllCondition } from '@/api/system'
import { condition } from '@/constants/commonConstants'
import i18n from '@/lang'
import { ConditionOptions } from '@/definitions/options'
import Cookies from 'js-cookie'

export default {
  name: 'Layout',
  components: {
    AppMain,
    Navbar,
    RightPanel,
    Settings,
    Sidebar,
    TagsView
  },
  mixins: [ResizeMixin],
  data() {
    return { ConditionOptions }
  },
  computed: {
    ...mapState({
      sidebar: state => state.app.sidebar,
      device: state => state.app.device,
      showSettings: state => state.settings.showSettings,
      needTagsView: state => state.settings.tagsView,
      fixedHeader: state => state.settings.fixedHeader
    }),
    classObj() {
      return {
        hideSidebar: !this.sidebar.opened,
        openSidebar: this.sidebar.opened,
        withoutAnimation: this.sidebar.withoutAnimation,
        mobile: this.device === 'mobile'
      }
    }
  },
  created() {
    this.getAllCondition()
  },
  methods: {
    handleClickOutside() {
      this.$store.dispatch('app/closeSideBar', { withoutAnimation: false })
    },
    getAllCondition() {
      getAllCondition().then(response => {
        const temp = []
        for (const responseKey of response) {
          if (!response.IsEnable) {
            temp.push({ id: responseKey.ConditionID, name: this.getI18n(responseKey.Name.toLowerCase()) })
          }
        }
        sessionStorage.setItem(condition, JSON.stringify(temp))
      })
    },
    getI18n(i18nString) {
      const temp = i18n.t(`symptom.${i18nString}`)
      return temp === `symptom.${i18nString}` ? i18nString : temp
    }
  }
}
</script>

